Graduate Groups are an important part of Graduate Students graduate research and education at UC Davis.  Currently, 47 out of 87 graduate programs at UCD are interdisciplinary Graduate Groups, drawing faculty from different departments across campus.   Some research groups further split into specific branches of study that are not listed here.  Often groups will work with other groups at different universities elsewhere in the world sharing data and research.  At the base group level, a common structure is a group of graduate students, postdocs and the occasional undergraduate working under a professor engaged in research or studies.

